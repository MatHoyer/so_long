#include "so_long.h"

int	modif_case(int x, int y, char **map)
{
	if (map[y][x] == '0')
		map[y][x] = '3';
	else if (map[y][x] == 'C')
		map[y][x] = '4';
	else if (map[y][x] == 'E')
		map[y][x] = '5';
	return (1);
}
